/*

Ques.3.Can we make a variable with the name switch? (Yes or No)

Ans.
No, we cannot make a variable with the name switch in C.

This is because switch is a reserved keyword in C, used to define a switch statement, which is a control flow structure. 
Since switch has a special meaning in the language, it cannot be used as an identifier (such as a variable name, function name, or type).

*/