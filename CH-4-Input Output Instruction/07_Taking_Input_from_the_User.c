#include<stdio.h>
int main()
{
    int a;
    scanf("%d",&a);                                                // &(ampersand)=address of
    printf("a=%d",a);
    return 0;
}







/*
------------------------------------------------------------Remember:--------------------------------------------------------------------------

1. Do not forget to mark address of operator before variable name in scanf function, it is important.

2. Also, do not accidently use address of operator in printf. 

*/
